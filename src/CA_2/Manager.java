public class Manager {
    private String name;
    private String type; // Example: Chief Surgeon, Head Nurse, Admin Manager

    public Manager(String name, String type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }
}